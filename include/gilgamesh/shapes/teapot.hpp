  // call this function to make a mesh
  // This works with the gilgamesh mesh, but is still generic.
  template <class Mesh>
  size_t build(Mesh &mesh, const glm::mat4 &transform = glm::mat4(), const glm::vec4 &color=glm::vec4(1), int num_subdivs=8) {
    //int vi = 0;
    auto vertex = [&](const glm::vec3 &pos, const glm::vec3 &normal, const glm::vec2 &uv) {
      mesh.addVertexTransformed(transform, pos, normal, uv, color);
    };

    auto index = [&](size_t idx) {
      mesh.addIndex(idx);
    };

    size_t first_index = mesh.vertices().size();

    buildMesh(vertex, index, first_index, num_subdivs);
    
    // return the first index of the mesh
    return first_index;
  }

  // call this function to generate vertices and indices.
  template <class Vertex, class Index>
  void buildMesh(Vertex vertex, Index index, size_t first_index, int num_subdivs) {
    const float *src = teapotData();
    int numPatches = (int)*src++;
    std::vector<glm::vec3> points;
    points.reserve(16);

    for (int patch = 0; patch != numPatches; ++patch) {
      int uorder = (int)*src++;
      int vorder = (int)*src++;
      points.resize(0);
      int imax = (uorder+1)*(vorder+1);
      for (int i = 0; i != imax; ++i) {
        points.emplace_back(src[0], src[1], src[2]);
        src += 3;
      }

      for (int i = 0; i <= num_subdivs; ++i) {
        float u = i * (1.0f/num_subdivs);
        float u0 = (1-u) * (1-u) * (1-u);
        float u1 = (1-u) * (1-u) * (u * 3);
        float u2 = (1-u) * u * (u * 3);
        float u3 = u * u * u;
        glm::vec3 p0 = points[ 0] * u0 + points[ 1] * u1 + points[ 2] * u2 + points[ 3] * u3;
        glm::vec3 p1 = points[ 4] * u0 + points[ 5] * u1 + points[ 6] * u2 + points[ 7] * u3;
        glm::vec3 p2 = points[ 8] * u0 + points[ 9] * u1 + points[10] * u2 + points[11] * u3;
        glm::vec3 p3 = points[12] * u0 + points[13] * u1 + points[14] * u2 + points[15] * u3;

        for (int j = 0; j <= num_subdivs; ++j) {
          float v = j * (1.0f/num_subdivs);
          float v0 = (1-v) * (1-v) * (1-v);
          float v1 = (1-v) * (1-v) * (v * 3);
          float v2 = (1-v) * v * (v * 3);
          float v3 = v * v * v;
          glm::vec3 p = p0 * v0 + p1 * v1 + p2 * v2 + p3 * v3;
          vertex(p, glm::vec3(), glm::vec2());
        }
      }
     
      int stride = (num_subdivs+1);
      for (int i = 0; i < num_subdivs; ++i) {
        for (int j = 0; j < num_subdivs; ++j) {
          size_t ix = first_index + i * (num_subdivs+1) + j;
          index(ix + 0 + stride * 0);
          index(ix + 0 + stride * 1);
          index(ix + 1 + stride * 0);
          index(ix + 1 + stride * 0);
          index(ix + 0 + stride * 1);
          index(ix + 1 + stride * 1);
        }
      }
      first_index += (num_subdivs+1) * (num_subdivs+1);
    }
  }
